import 'package:design_builder/src/models/theme_config.dart';
import 'package:design_builder/src/models/token_group.dart';
import 'package:design_builder/src/models/token_group_node.dart';
import 'package:design_builder/src/parsers/token_parser.dart';

/// Generates the AppTheme Dart code from design tokens.
///
/// This generator produces a unified theme file that supports multiple modes
/// (e.g., light/dark) with server-side override capabilities. The output
/// prioritizes developer experience by placing all class definitions first,
/// followed by const lookup maps at the end of the file.
///
/// Features:
/// - Const lookup maps for O(1) token resolution.
/// - Mode-aware unified lookup methods in _TokenStore.
/// - Server override support for dynamic theming.
/// - Non-nullable getters with fallback values.
/// - Recursive nesting support for arbitrary token depths.
class ThemeGenerator {
  ThemeGenerator(this.config);
  final ThemeConfig config;

  /// Map of group paths to their descriptions from the token file
  Map<String, String> _groupDescriptions = {};

  /// Primary mode (first mode in the list, used for lookups)
  String _primaryMode = 'light';

  /// Generates the complete Dart source file for the theme.
  ///
  /// The generated file is structured as follows:
  /// 1. File header and imports.
  /// 2. Main [AppTheme] class.
  /// 3. [AppThemeProvider] for state management.
  /// 4. [_TokenStore] for token lookup and overrides.
  /// 5. [BuildContext] extension for easy access.
  /// 6. Theme helper classes (_ThemeColors, _ThemeSizes, _ThemeTypography).
  /// 7. Category resolver classes (e.g., _BrandColors, _FontSizes, _DisplayTypography).
  /// 8. Const lookup maps (placed at the bottom for readability).
  ///
  /// Parameters:
  /// - [modeGroups]: Token groups organized by mode.
  /// - [modeTokens]: Design tokens organized by mode.
  /// - [modes]: List of supported mode names (e.g., ['dark', 'light']).
  /// - [groupDescriptions]: Map of group paths to their descriptions.
  /// - [rootDescription]: Root-level description from the token file.
  String generateUnified(
    Map<String, List<TokenGroup>> modeGroups,
    Map<String, List<DesignToken>> modeTokens,
    List<String> modes,
    Map<String, String> groupDescriptions,
    String? rootDescription,
  ) {
    _groupDescriptions = groupDescriptions;
    _primaryMode = modes.first;
    final buffer = StringBuffer();
    final primaryMode = modes.first;

    // Build a hierarchical tree structure from the flat groups
    final colorTree = _buildCategoryTree(
      modeGroups[primaryMode] ?? [],
      'color',
    );
    final sizeTree = _buildCategoryTree(modeGroups[primaryMode] ?? [], 'sizes');
    final typographyTree = _buildCategoryTree(
      modeGroups[primaryMode] ?? [],
      'typography',
    );

    // --- File Header ---
    buffer.writeln('// GENERATED CODE - DO NOT MODIFY MANUALLY');
    buffer.writeln('// Generated by design_builder');
    if (rootDescription != null && rootDescription.isNotEmpty) {
      buffer.writeln('//');
      final wrappedLines = _wrapText(rootDescription, maxWidth: 78, prefix: '// ');
      for (final line in wrappedLines) {
        buffer.writeln(line);
      }
    }
    buffer.writeln('//');
    buffer.writeln(
      '// This file contains theme configuration generated from design tokens.',
    );
    buffer.writeln('// To update, modify the source token files and rebuild.');
    buffer.writeln('//');
    buffer.writeln('// ignore_for_file: library_private_types_in_public_api, unnecessary_getters_setters');
    buffer.writeln();
    buffer.writeln("import 'package:flutter/material.dart';");
    buffer.writeln();

    // --- Main Theme Classes ---
    buffer.writeln(_generateAppTheme(colorTree, sizeTree, typographyTree));
    buffer.writeln();

    buffer.writeln(_generateThemeProvider(modes));
    buffer.writeln();

    buffer.writeln(
      _generateTokenStore(modes, colorTree, sizeTree, typographyTree),
    );
    buffer.writeln();

    buffer.writeln(_generateContextExtensions());
    buffer.writeln();

    // --- Helper and Resolver Classes ---
    buffer.writeln(
      _generateThemeHelperClasses(colorTree, sizeTree, typographyTree),
    );
    buffer.writeln();

    // Generate color resolver classes recursively.
    for (final node in colorTree) {
      buffer.writeln(_generateColorResolvers(node, modes, modeTokens));
    }
    if (colorTree.isNotEmpty) buffer.writeln();

    // Generate size resolver classes recursively.
    for (final node in sizeTree) {
      buffer.writeln(_generateSizeResolvers(node, modes, modeTokens));
    }
    if (sizeTree.isNotEmpty) buffer.writeln();

    // Generate typography resolver classes recursively.
    for (final node in typographyTree) {
      buffer.writeln(_generateTypographyResolvers(node, modes, modeTokens));
    }
    if (typographyTree.isNotEmpty) buffer.writeln();

    // --- Const Data (placed at the end for readability) ---
    // These maps contain the raw token values and are referenced by the
    // resolver classes above. Placing them at the bottom keeps the
    // API surface (classes) visible first when opening the file.
    buffer.writeln(
      _generateLookupMaps(
        modes,
        modeTokens,
        colorTree,
        sizeTree,
        typographyTree,
      ),
    );

    return buffer.toString();
  }

  /// Build a hierarchical tree from flat groups for a specific category.
  /// This organizes groups by their parent-child relationships.
  List<TokenGroupNode> _buildCategoryTree(
    List<TokenGroup> groups,
    String category,
  ) {
    final categoryGroups = groups
        .where((g) => g.category.toLowerCase() == category.toLowerCase())
        .toList();

    // Build tree structure
    final rootNodes = <TokenGroupNode>[];
    final nodeMap = <String, TokenGroupNode>{};

    // First pass: create all nodes
    for (final group in categoryGroups) {
      final fullPath = group.fullGroupPath;
      final node = TokenGroupNode(
        name: group.name,
        fullPath: fullPath,
        group: group,
        children: [],
      );
      nodeMap[fullPath] = node;
    }

    // Second pass: establish parent-child relationships
    for (final group in categoryGroups) {
      final fullPath = group.fullGroupPath;
      final node = nodeMap[fullPath]!;

      // Check if this is a root node or has a parent
      if (!group.isNested) {
        rootNodes.add(node);
      } else {
        // Find parent
        final parentPath = group.parent!.fullGroupPath;
        final parentNode = nodeMap[parentPath];
        if (parentNode != null) {
          parentNode.children.add(node);
        }
      }
    }

    // Sort root nodes for consistent output
    rootNodes.sort((a, b) => a.name.compareTo(b.name));

    return rootNodes;
  }

  /// Generates the const lookup maps containing raw token values.
  ///
  /// Creates one map per category group per mode:
  /// - `_{mode}Color{GroupName}` for colors.
  /// - `_{mode}Size{GroupName}` for dimensions.
  /// - `_{mode}Typography{GroupName}` for text styles.
  String _generateLookupMaps(
    List<String> modes,
    Map<String, List<DesignToken>> modeTokens,
    List<TokenGroupNode> colorTree,
    List<TokenGroupNode> sizeTree,
    List<TokenGroupNode> typographyTree,
  ) {
    final buffer = StringBuffer();

    for (final mode in modes) {
      final tokens = modeTokens[mode] ?? [];

      // Generate color maps recursively.
      for (final node in colorTree) {
        _generateColorMapsRecursive(buffer, mode, node, tokens);
      }

      // Generate size maps recursively.
      for (final node in sizeTree) {
        _generateSizeMapsRecursive(buffer, mode, node, tokens);
      }

      // Generate typography maps recursively.
      for (final node in typographyTree) {
        _generateTypographyMapsRecursive(buffer, mode, node, tokens);
      }
    }

    return buffer.toString();
  }

  void _generateColorMapsRecursive(
    StringBuffer buffer,
    String mode,
    TokenGroupNode node,
    List<DesignToken> tokens,
  ) {
    // Get all tokens that belong to this node (including from descendants)
    // Compare paths without mode prefix for cross-mode matching
    final allPaths = _getAllTokenPaths(
      node,
    ).map((p) => _stripModePrefix(p)).toList();
    final groupTokens = tokens
        .where((t) => allPaths.any((p) => _stripModePrefix(t.path) == p))
        .toList();

    if (groupTokens.isNotEmpty) {
      final mapName = '_${mode}Color${_pascalCasePath(node.fullPath)}';
      buffer.writeln();
      buffer.writeln('const Map<String, Color> $mapName = <String, Color>{');
      for (final t in groupTokens) {
        final tokenName = t.path.split('.').last;
        final colorVal = TokenParser.parseColor(t.value);
        if (colorVal != null) {
          buffer.writeln(
            "  '$tokenName': Color(0x${colorVal.toRadixString(16).padLeft(8, '0').toUpperCase()}),",
          );
        }
      }
      buffer.writeln('};');
    }

    // Recurse into children
    for (final child in node.children) {
      _generateColorMapsRecursive(buffer, mode, child, tokens);
    }
  }

  void _generateSizeMapsRecursive(
    StringBuffer buffer,
    String mode,
    TokenGroupNode node,
    List<DesignToken> tokens,
  ) {
    // Compare paths without mode prefix for cross-mode matching
    final allPaths = _getAllTokenPaths(
      node,
    ).map((p) => _stripModePrefix(p)).toList();
    final groupTokens = tokens
        .where((t) => allPaths.any((p) => _stripModePrefix(t.path) == p))
        .toList();

    if (groupTokens.isNotEmpty) {
      final mapName = '_${mode}Size${_pascalCasePath(node.fullPath)}';
      buffer.writeln();
      buffer.writeln('const Map<String, double> $mapName = <String, double>{');
      for (final t in groupTokens) {
        final tokenName = t.path.split('.').last;
        final numValue = t.value is num
            ? (t.value as num).toDouble()
            : (t.value is String
                  ? double.tryParse(
                      (t.value as String).replaceAll(
                        RegExp(r'(px|rem|em)$'),
                        '',
                      ),
                    )
                  : null);
        if (numValue != null) {
          buffer.writeln("  '$tokenName': $numValue,");
        }
      }
      buffer.writeln('};');
    }

    for (final child in node.children) {
      _generateSizeMapsRecursive(buffer, mode, child, tokens);
    }
  }

  void _generateTypographyMapsRecursive(
    StringBuffer buffer,
    String mode,
    TokenGroupNode node,
    List<DesignToken> tokens,
  ) {
    // Only generate maps for leaf nodes (groups with actual tokens)
    if (node.group.tokenPaths.isNotEmpty) {
      final mapName = '_${mode}Typography${_pascalCasePath(node.fullPath)}';
      // Compare paths without mode prefix for cross-mode matching
      final groupPaths = node.group.tokenPaths
          .map((p) => _stripModePrefix(p))
          .toList();
      final groupTokens = tokens
          .where((t) => groupPaths.contains(_stripModePrefix(t.path)))
          .toList();

      if (groupTokens.isNotEmpty) {
        buffer.writeln();
        buffer.writeln(
          'const Map<String, TextStyle> $mapName = <String, TextStyle>{',
        );
        for (final t in groupTokens) {
          final tokenName = t.path.split('.').last;
          final textStyle = _generateTextStyle(t.value);
          if (textStyle != null) {
            buffer.writeln("  '$tokenName': $textStyle,");
          }
        }
        buffer.writeln('};');
      }
    }

    // Recurse into children
    for (final child in node.children) {
      _generateTypographyMapsRecursive(buffer, mode, child, tokens);
    }
  }

  List<String> _getAllTokenPaths(TokenGroupNode node) {
    final result = <String>[...node.group.tokenPaths];
    for (final child in node.children) {
      result.addAll(_getAllTokenPaths(child));
    }
    return result;
  }

  /// Generates the _TokenStore class for managing theme tokens and overrides.
  ///
  /// This class provides the core lookup logic:
  /// - Checks server overrides first.
  /// - Falls back to const maps based on current mode.
  /// - Supports colors, sizes, and typography.
  String _generateTokenStore(
    List<String> modes,
    List<TokenGroupNode> colorTree,
    List<TokenGroupNode> sizeTree,
    List<TokenGroupNode> typographyTree,
  ) {
    final buffer = StringBuffer();

    buffer.writeln(
      '/// Manages theme token storage and server-side overrides.',
    );
    buffer.writeln('///');
    buffer.writeln(
      '/// The store maintains a current mode (e.g., "dark", "light") and a map',
    );
    buffer.writeln(
      '/// of overrides. All lookups check overrides first, then fall back',
    );
    buffer.writeln('/// to the const lookup maps for the current mode.');
    buffer.writeln('class _TokenStore {');
    buffer.writeln('  String _mode;');
    buffer.writeln('  Map<String, dynamic> _overrides = const {};');
    buffer.writeln();
    buffer.writeln('  _TokenStore({required String mode}) : _mode = mode;');
    buffer.writeln();
    buffer.writeln('  String get mode => _mode;');
    buffer.writeln('  set mode(String value) => _mode = value;');
    buffer.writeln();
    buffer.writeln('  Map<String, dynamic> get overrides => _overrides;');
    buffer.writeln();
    buffer.writeln('  void setOverrides(Map<String, dynamic> overrides) {');
    buffer.writeln('    _overrides = Map.unmodifiable(overrides);');
    buffer.writeln('  }');
    buffer.writeln();
    buffer.writeln('  void clearOverrides() {');
    buffer.writeln('    _overrides = const {};');
    buffer.writeln('  }');
    buffer.writeln();

    // Generate color lookup method with nested group support.
    buffer.writeln('  /// Looks up a color by full group path and token name.');
    buffer.writeln('  ///');
    buffer.writeln(
      '  /// Returns the override if one exists for the full path, otherwise',
    );
    buffer.writeln(
      '  /// returns the value from the const map for the current mode.',
    );
    buffer.writeln('  Color? color(String groupPath, String token) {');
    buffer.writeln('    // Check override first using the full token path.');
    buffer.writeln('    final path = "\$_mode.color.\$groupPath.\$token";');
    buffer.writeln('    final override = _overrides[path];');
    buffer.writeln('    if (override is Color) return override;');
    buffer.writeln();
    buffer.writeln('    // Lookup in the appropriate mode map.');
    buffer.writeln(
      r"    final pascalPath = groupPath.split('.').map((s) => _pascalCase(s)).join();",
    );
    buffer.writeln("    if (_mode == 'dark') {");
    buffer.writeln('      switch (pascalPath) {');
    _generateColorCaseStatements(buffer, colorTree, 'dark');
    buffer.writeln('      }');
    buffer.writeln('    } else {');
    buffer.writeln('      switch (pascalPath) {');
    _generateColorCaseStatements(buffer, colorTree, 'light');
    buffer.writeln('      }');
    buffer.writeln('    }');
    buffer.writeln('    return null;');
    buffer.writeln('  }');
    buffer.writeln();

    // Generate size lookup method.
    buffer.writeln(
      '  /// Looks up a size dimension by full group path and token name.',
    );
    buffer.writeln('  ///');
    buffer.writeln(
      '  /// Returns the override if one exists, otherwise returns the value',
    );
    buffer.writeln('  /// from the const map for the current mode.');
    buffer.writeln('  double? size(String groupPath, String token) {');
    buffer.writeln('    final path = "\$_mode.sizes.\$groupPath.\$token";');
    buffer.writeln('    final override = _overrides[path];');
    buffer.writeln('    if (override is double) return override;');
    buffer.writeln();
    buffer.writeln(
      r"    final pascalPath = groupPath.split('.').map((s) => _pascalCase(s)).join();",
    );
    buffer.writeln("    if (_mode == 'dark') {");
    buffer.writeln('      switch (pascalPath) {');
    _generateSizeCaseStatements(buffer, sizeTree, 'dark');
    buffer.writeln('      }');
    buffer.writeln('    } else {');
    buffer.writeln('      switch (pascalPath) {');
    _generateSizeCaseStatements(buffer, sizeTree, 'light');
    buffer.writeln('      }');
    buffer.writeln('    }');
    buffer.writeln('    return null;');
    buffer.writeln('  }');
    buffer.writeln();

    // Generate typography lookup method.
    buffer.writeln(
      '  /// Looks up a text style by full group path and token name.',
    );
    buffer.writeln('  ///');
    buffer.writeln(
      '  /// Returns the override if one exists, otherwise returns the style',
    );
    buffer.writeln('  /// from the const map for the current mode.');
    buffer.writeln('  TextStyle? textStyle(String groupPath, String token) {');
    buffer.writeln(
      '    final path = "\$_mode.typography.\$groupPath.\$token";',
    );
    buffer.writeln('    final override = _overrides[path];');
    buffer.writeln('    if (override is TextStyle) return override;');
    buffer.writeln();
    buffer.writeln(
      r"    final pascalPath = groupPath.split('.').map((s) => _pascalCase(s)).join();",
    );
    buffer.writeln("    if (_mode == 'dark') {");
    buffer.writeln('      switch (pascalPath) {');
    _generateTypographyCaseStatements(buffer, typographyTree, 'dark');
    buffer.writeln('      }');
    buffer.writeln('    } else {');
    buffer.writeln('      switch (pascalPath) {');
    _generateTypographyCaseStatements(buffer, typographyTree, 'light');
    buffer.writeln('      }');
    buffer.writeln('    }');
    buffer.writeln('    return null;');
    buffer.writeln('  }');

    // Add helper method for pascal case conversion
    buffer.writeln();
    buffer.writeln('  /// Converts a string to PascalCase.');
    buffer.writeln('  String _pascalCase(String input) {');
    buffer.writeln('    if (input.isEmpty) return input;');
    buffer.writeln('    return input[0].toUpperCase() + input.substring(1);');
    buffer.writeln('  }');
    buffer.writeln('}');

    return buffer.toString();
  }

  void _generateColorCaseStatements(
    StringBuffer buffer,
    List<TokenGroupNode> tree,
    String mode, {
    String prefix = '',
  }) {
    for (final node in tree) {
      final pascalName = _pascalCasePath(node.fullPath);
      if (node.children.isEmpty) {
        // Leaf node
        buffer.writeln(
          '        case "$pascalName": return _${mode}Color$pascalName[token];',
        );
      }
      // Recurse into children
      _generateColorCaseStatements(buffer, node.children, mode, prefix: prefix);
    }
  }

  void _generateSizeCaseStatements(
    StringBuffer buffer,
    List<TokenGroupNode> tree,
    String mode, {
    String prefix = '',
  }) {
    for (final node in tree) {
      final pascalName = _pascalCasePath(node.fullPath);
      if (node.children.isEmpty) {
        buffer.writeln(
          '        case "$pascalName": return _${mode}Size$pascalName[token];',
        );
      }
      _generateSizeCaseStatements(buffer, node.children, mode, prefix: prefix);
    }
  }

  void _generateTypographyCaseStatements(
    StringBuffer buffer,
    List<TokenGroupNode> tree,
    String mode, {
    String prefix = '',
  }) {
    for (final node in tree) {
      final pascalName = _pascalCasePath(node.fullPath);
      // Typography generates maps for leaf nodes with tokens
      if (node.group.tokenPaths.isNotEmpty) {
        buffer.writeln(
          '        case "$pascalName": return _${mode}Typography$pascalName[token];',
        );
      }
      _generateTypographyCaseStatements(
        buffer,
        node.children,
        mode,
        prefix: prefix,
      );
    }
  }

  /// Generates color resolver classes recursively for the tree structure.
  String _generateColorResolvers(
    TokenGroupNode node,
    List<String> modes,
    Map<String, List<DesignToken>> modeTokens,
  ) {
    final buffer = StringBuffer();

    // Generate the resolver class for this node
    final className = '_${_pascalCasePath(node.fullPath)}Colors';
    final fields =
        node.group.tokenPaths.map((p) => _fieldNameFromPath(p)).toSet().toList()
          ..sort();

    // Only generate class if it has fields or children
    if (fields.isNotEmpty || node.children.isNotEmpty) {
      // Use group description if available, otherwise use default
      final groupDesc = _groupDescriptions['$_primaryMode.color.${node.fullPath}'];
      if (groupDesc != null && groupDesc.isNotEmpty) {
        final wrappedLines = _wrapText(groupDesc, maxWidth: 78, prefix: '/// ');
        for (final line in wrappedLines) {
          buffer.writeln(line);
        }
      } else {
        buffer.writeln(
          '/// Provides typed access to the "${node.fullPath}" color tokens.',
        );
      }
      buffer.writeln('class $className {');
      buffer.writeln('  final _TokenStore _store;');
      buffer.writeln();
      buffer.writeln('  const $className(this._store);');
      buffer.writeln();

      // Generate getters for direct tokens
      final primaryMode = modes.first;
      final primaryTokens = modeTokens[primaryMode] ?? [];
      for (final field in fields) {
        // Find the token for this field to get the description
        final token = primaryTokens.firstWhere(
          (t) => t.path.endsWith('.${node.fullPath}.$field'),
          orElse: () => DesignToken(
            path: '${node.fullPath}.$field',
            type: 'color',
            value: '',
            raw: {},
          ),
        );
        // Add description comment if available
        if (token.description != null && token.description!.isNotEmpty) {
          buffer.writeln('  /// ${token.description}');
        }
        buffer.writeln(
          "  Color get $field => _store.color('${node.fullPath}', '$field')!;",
        );
      }

      // Generate sub-accessors for children
      for (final child in node.children) {
        final childClass = '_${_pascalCasePath(child.fullPath)}Colors';
        final childField = _camelCase(child.name);
        buffer.writeln('  $childClass get $childField => $childClass(_store);');
      }

      buffer.writeln('}');
    }

    // Recursively generate classes for children
    for (final child in node.children) {
      buffer.writeln();
      buffer.writeln(_generateColorResolvers(child, modes, modeTokens));
    }

    return buffer.toString();
  }

  /// Generates size resolver classes recursively for the tree structure.
  String _generateSizeResolvers(
    TokenGroupNode node,
    List<String> modes,
    Map<String, List<DesignToken>> modeTokens,
  ) {
    final buffer = StringBuffer();

    final className = '_${_pascalCasePath(node.fullPath)}Sizes';
    final fields =
        node.group.tokenPaths.map((p) => _fieldNameFromPath(p)).toSet().toList()
          ..sort();

    final primaryMode = modes.first;
    final primaryTokens = modeTokens[primaryMode] ?? [];

    if (fields.isNotEmpty || node.children.isNotEmpty) {
      // Use group description if available, otherwise use default
      final groupDesc = _groupDescriptions['$_primaryMode.sizes.${node.fullPath}'];
      if (groupDesc != null && groupDesc.isNotEmpty) {
        final wrappedLines = _wrapText(groupDesc, maxWidth: 78, prefix: '/// ');
        for (final line in wrappedLines) {
          buffer.writeln(line);
        }
      } else {
        buffer.writeln(
          '/// Provides typed access to the "${node.fullPath}" size tokens.',
        );
      }
      buffer.writeln('class $className {');
      buffer.writeln('  final _TokenStore _store;');
      buffer.writeln();
      buffer.writeln('  const $className(this._store);');
      buffer.writeln();

      // Generate getters for direct tokens with fallback values
      for (final field in fields) {
        // Find the token for this field to get the fallback value
        final token = primaryTokens.firstWhere(
          (t) => t.path.endsWith('.${node.fullPath}.$field'),
          orElse: () => DesignToken(
            path: '${node.fullPath}.$field',
            type: 'dimension',
            value: 0.0,
            raw: {},
          ),
        );

        double fallbackValue;
        if (token.value is num) {
          fallbackValue = (token.value as num).toDouble();
        } else if (token.value is String) {
          final parsed = double.tryParse(
            (token.value as String).replaceAll(RegExp(r'(px|rem|em)$'), ''),
          );
          fallbackValue = parsed ?? 0.0;
        } else {
          fallbackValue = 0.0;
        }

        // Add description comment if available
        if (token.description != null && token.description!.isNotEmpty) {
          buffer.writeln('  /// ${token.description}');
        }
        buffer.writeln(
          "  double get $field => _store.size('${node.fullPath}', '$field') ?? $fallbackValue;",
        );
      }

      // Generate sub-accessors for children
      for (final child in node.children) {
        final childClass = '_${_pascalCasePath(child.fullPath)}Sizes';
        final childField = _camelCase(child.name);
        buffer.writeln('  $childClass get $childField => $childClass(_store);');
      }

      buffer.writeln('}');
    }

    // Recursively generate classes for children
    for (final child in node.children) {
      buffer.writeln();
      buffer.writeln(_generateSizeResolvers(child, modes, modeTokens));
    }

    return buffer.toString();
  }

  /// Generates typography resolver classes recursively for the tree structure.
  String _generateTypographyResolvers(
    TokenGroupNode node,
    List<String> modes,
    Map<String, List<DesignToken>> modeTokens,
  ) {
    final buffer = StringBuffer();

    final className = '_${_pascalCasePath(node.fullPath)}Typography';
    final fields =
        node.group.tokenPaths.map((p) => _fieldNameFromPath(p)).toSet().toList()
          ..sort();

    final primaryMode = modes.first;
    final primaryTokens = modeTokens[primaryMode] ?? [];

    if (fields.isNotEmpty || node.children.isNotEmpty) {
      // Use group description if available, otherwise use default
      final groupDesc = _groupDescriptions['$_primaryMode.typography.${node.fullPath}'];
      if (groupDesc != null && groupDesc.isNotEmpty) {
        final wrappedLines = _wrapText(groupDesc, maxWidth: 78, prefix: '/// ');
        for (final line in wrappedLines) {
          buffer.writeln(line);
        }
      } else {
        buffer.writeln(
          '/// Provides typed access to the "${node.fullPath}" typography tokens.',
        );
      }
      buffer.writeln('class $className {');
      buffer.writeln('  final _TokenStore _store;');
      buffer.writeln();
      buffer.writeln('  const $className(this._store);');
      buffer.writeln();

      // Generate getters for direct tokens with fallback TextStyle values
      for (final field in fields) {
        final token = primaryTokens.firstWhere(
          (t) => t.path.endsWith('.${node.fullPath}.$field'),
          orElse: () => DesignToken(
            path: '${node.fullPath}.$field',
            type: 'typography',
            value: {'fontSize': 14.0, 'fontWeight': 400},
            raw: {},
          ),
        );

        String fallbackValue;
        final textStyle = _generateTextStyle(token.value);
        if (textStyle != null) {
          fallbackValue = textStyle;
        } else {
          fallbackValue = 'const TextStyle(fontSize: 14)';
        }

        // Add description comment if available
        if (token.description != null && token.description!.isNotEmpty) {
          buffer.writeln('  /// ${token.description}');
        }
        buffer.writeln(
          "  TextStyle get $field => _store.textStyle('${node.fullPath}', '$field') ?? $fallbackValue;",
        );
      }

      // Generate sub-accessors for children
      for (final child in node.children) {
        final childClass = '_${_pascalCasePath(child.fullPath)}Typography';
        final childField = _camelCase(child.name);
        buffer.writeln('  $childClass get $childField => $childClass(_store);');
      }

      buffer.writeln('}');
    }

    // Recursively generate classes for children
    for (final child in node.children) {
      buffer.writeln();
      buffer.writeln(_generateTypographyResolvers(child, modes, modeTokens));
    }

    return buffer.toString();
  }

