  /// Generates the ${config.className}Notifier and ${config.className}Provider for state management.
  ///
  /// Creates:
  /// - [${config.className}Notifier]: A ChangeNotifier that manages the current theme mode
  ///   and applies server-side overrides. It provides themeData for MaterialApp
  ///   and methods to toggle modes and manage overrides.
  /// - [${config.className}Provider]: An InheritedNotifier widget that provides
  ///   [${config.className}Notifier] to the widget tree via `of(context)`.
  String _generateThemeProvider(List<String> modes) {
    final buffer = StringBuffer();
    final modeEnum = modes.map((m) => m.toLowerCase()).join(', ');
    final className = config.className;
    final notifierClassName = '${className}Notifier';
    final providerClassName = '${className}Provider';
    final modeClassName = '${className}Mode';

    buffer.writeln('/// Available theme modes.');
    buffer.writeln('enum $modeClassName { $modeEnum }');
    buffer.writeln();

    buffer.writeln('/// Manages theme state and server-side overrides.');
    buffer.writeln('///');
    buffer.writeln(
      '/// Use this as a ChangeNotifier in your widget tree. It provides',
    );
    buffer.writeln(
      '/// [themeData] for MaterialApp and handles mode switching and',
    );
    buffer.writeln('/// applying server overrides dynamically.');
    buffer.writeln('class $notifierClassName extends ChangeNotifier {');
    buffer.writeln(
      '  $notifierClassName({$modeClassName initialMode = $modeClassName.dark})',
    );
    buffer.writeln('      : _mode = initialMode,');
    buffer.writeln('        _store = _TokenStore(mode: initialMode.name),');
    buffer.writeln(
      '        _theme = $className(_TokenStore(mode: initialMode.name));',
    );
    buffer.writeln();
    buffer.writeln('  $modeClassName _mode;');
    buffer.writeln('  final _TokenStore _store;');
    buffer.writeln('$className _theme;');
    buffer.writeln();
    buffer.writeln('  /// The current theme mode (dark or light).');
    buffer.writeln('  $modeClassName get mode => _mode;');
    buffer.writeln('  /// The current $className instance.');
    buffer.writeln('  $className get theme => _theme;');
    buffer.writeln();

    // Generate themeData getter.
    buffer.writeln(
      '  /// Material ThemeData derived from the current $className.',
    );
    buffer.writeln('  ThemeData get themeData {');
    buffer.writeln('    final isDark = _mode == $modeClassName.dark;');
    buffer.writeln('    return ThemeData(');
    buffer.writeln(
      '      brightness: isDark ? Brightness.dark : Brightness.light,',
    );
    buffer.writeln('      extensions: [_theme],');
    buffer.writeln('    );');
    buffer.writeln('  }');
    buffer.writeln();

    // Mode setter.
    buffer.writeln('  set mode($modeClassName mode) {');
    buffer.writeln('    if (_mode != mode) {');
    buffer.writeln('      _mode = mode;');
    buffer.writeln('      _store.mode = mode.name;');
    buffer.writeln('      _theme = $className(_store);');
    buffer.writeln('      notifyListeners();');
    buffer.writeln('    }');
    buffer.writeln('  }');
    buffer.writeln();

    // Toggle method.
    buffer.writeln('  /// Toggles between light and dark modes.');
    buffer.writeln('  void toggleMode() {');
    buffer.writeln(
      '    mode = _mode == $modeClassName.light ? $modeClassName.dark : $modeClassName.light;',
    );
    buffer.writeln('  }');
    buffer.writeln();

    // Apply overrides.
    buffer.writeln('  /// Applies server-side theme overrides.');
    buffer.writeln('  ///');
    buffer.writeln(
      '  /// [overrides] is a map of token paths to values, e.g.,',
    );
    buffer.writeln(
      '  /// `{ "dark.color.brand.primary": Color(0xFFFF0000) }`.',
    );
    buffer.writeln('  void applyOverrides(Map<String, dynamic> overrides) {');
    buffer.writeln(
      '    final merged = Map<String, dynamic>.from(_store.overrides);',
    );
    buffer.writeln('    merged.addAll(overrides);');
    buffer.writeln('    _store.setOverrides(merged);');
    buffer.writeln('    _theme = $className(_store);');
    buffer.writeln('    notifyListeners();');
    buffer.writeln('  }');
    buffer.writeln();

    // Clear overrides.
    buffer.writeln('  /// Clears all server-side overrides.');
    buffer.writeln('  void clearOverrides() {');
    buffer.writeln('    _store.clearOverrides();');
    buffer.writeln('    _theme = $className(_store);');
    buffer.writeln('    notifyListeners();');
    buffer.writeln('  }');
    buffer.writeln('}');
    buffer.writeln();

    // ${className}Provider (InheritedNotifier)
    buffer.writeln(
      '/// Provides [$notifierClassName] to the widget tree via [InheritedNotifier].',
    );
    buffer.writeln('///');
    buffer.writeln(
      '/// Use this widget to inject theme state into your app. Access the notifier',
    );
    buffer.writeln(
      '/// via `$providerClassName.of(context)` or use the extension methods on [BuildContext].',
    );
    buffer.writeln('///');
    buffer.writeln(
      '/// When using the [builder] constructor, widgets rebuild automatically when theme changes.',
    );
    buffer.writeln(
      'class $providerClassName extends InheritedNotifier<$notifierClassName> {',
    );
    buffer.writeln('  const $providerClassName({');
    buffer.writeln('    super.key,');
    buffer.writeln('    required super.notifier,');
    buffer.writeln('    required super.child,');
    buffer.writeln('  });');
    buffer.writeln();

    buffer.writeln(
      '  /// Creates a [$providerClassName] with a builder that rebuilds when theme changes.',
    );
    buffer.writeln('  ///');
    buffer.writeln(
      '  /// The [builder] callback is called whenever the theme notifier notifies listeners.',
    );
    buffer.writeln(
      '  /// This eliminates the need for [AnimatedBuilder] or similar widgets.',
    );
    buffer.writeln('  static Widget builder({');
    buffer.writeln('    Key? key,');
    buffer.writeln('    required $notifierClassName notifier,');
    buffer.writeln(
      '    required Widget Function(BuildContext context, $notifierClassName notifier) builder,',
    );
    buffer.writeln('  }) {');
    buffer.writeln('    return $providerClassName(');
    buffer.writeln('      key: key,');
    buffer.writeln('      notifier: notifier,');
    buffer.writeln('      child: _${className}Builder(');
    buffer.writeln('        notifier: notifier,');
    buffer.writeln('        builder: builder,');
    buffer.writeln('      ),');
    buffer.writeln('    );');
    buffer.writeln('  }');
    buffer.writeln();

    buffer.writeln(
      '  /// Retrieves the [$notifierClassName] from the widget tree.',
    );
    buffer.writeln('  ///');
    buffer.writeln(
      '  /// Returns null if no [$providerClassName] is found in the ancestry.',
    );
    buffer.writeln(
      '  static $notifierClassName? maybeOf(BuildContext context) {',
    );
    buffer.writeln(
      '    return context.dependOnInheritedWidgetOfExactType<$providerClassName>()?.notifier;',
    );
    buffer.writeln('  }');
    buffer.writeln();

    buffer.writeln(
      '  /// Retrieves the [$notifierClassName] from the widget tree.',
    );
    buffer.writeln('  ///');
    buffer.writeln(
      '  /// Throws an error if no [$providerClassName] is found in the ancestry.',
    );
    buffer.writeln('  static $notifierClassName of(BuildContext context) {');
    buffer.writeln('    final notifier = maybeOf(context);');
    buffer.writeln(
      "    assert(notifier != null, 'No $providerClassName found in context');",
    );
    buffer.writeln('    return notifier!;');
    buffer.writeln('  }');
    buffer.writeln('}');
    buffer.writeln();

    // _${className}Builder helper widget
    buffer.writeln(
      '/// Internal widget that rebuilds when [$notifierClassName] changes.',
    );
    buffer.writeln('class _${className}Builder extends StatefulWidget {');
    buffer.writeln('  final $notifierClassName notifier;');
    buffer.writeln(
      '  final Widget Function(BuildContext context, $notifierClassName notifier) builder;',
    );
    buffer.writeln();
    buffer.writeln('  const _${className}Builder({');
    buffer.writeln('    required this.notifier,');
    buffer.writeln('    required this.builder,');
    buffer.writeln('  });');
    buffer.writeln();
    buffer.writeln('  @override');
    buffer.writeln(
      '  State<_${className}Builder> createState() => _${className}BuilderState();',
    );
    buffer.writeln('}');
    buffer.writeln();

    buffer.writeln(
      'class _${className}BuilderState extends State<_${className}Builder> {',
    );
    buffer.writeln('  @override');
    buffer.writeln('  void initState() {');
    buffer.writeln('    super.initState();');
    buffer.writeln('    widget.notifier.addListener(_onThemeChanged);');
    buffer.writeln('  }');
    buffer.writeln();
    buffer.writeln('  @override');
    buffer.writeln('  void didUpdateWidget(_${className}Builder oldWidget) {');
    buffer.writeln('    super.didUpdateWidget(oldWidget);');
    buffer.writeln('    if (widget.notifier != oldWidget.notifier) {');
    buffer.writeln('      oldWidget.notifier.removeListener(_onThemeChanged);');
    buffer.writeln('      widget.notifier.addListener(_onThemeChanged);');
    buffer.writeln('    }');
    buffer.writeln('  }');
    buffer.writeln();
    buffer.writeln('  @override');
    buffer.writeln('  void dispose() {');
    buffer.writeln('    widget.notifier.removeListener(_onThemeChanged);');
    buffer.writeln('    super.dispose();');
    buffer.writeln('  }');
    buffer.writeln();
    buffer.writeln('  void _onThemeChanged() {');
    buffer.writeln('    setState(() {});');
    buffer.writeln('  }');
    buffer.writeln();
    buffer.writeln('  @override');
    buffer.writeln('  Widget build(BuildContext context) {');
    buffer.writeln('    return widget.builder(context, widget.notifier);');
    buffer.writeln('  }');
    buffer.writeln('}');

    return buffer.toString();
  }

  /// Generates the BuildContext extension for easy theme access.
  ///
  /// Adds:
  /// - `context.theme` getter that retrieves the ${config.className} from
  ///   the current build context's ThemeData extensions.
  /// - `context.themeNotifier` getter that retrieves the ${config.className}Notifier
  ///   from the ${config.className}Provider in the widget tree.
  String _generateContextExtensions() {
    final className = config.className;
    final notifierClassName = '${className}Notifier';
    final providerClassName = '${className}Provider';

    return '''
/// Extension methods on [BuildContext] for convenient theme access.
extension ${className}ContextExtensions on BuildContext {
  /// Retrieves the [$className] from the current context.
  ///
  /// This is equivalent to `Theme.of(context).extension<$className>()!` but
  /// provides a more concise syntax: `context.theme`.
  $className get theme {
    return Theme.of(this).extension<$className>()!;
  }

  /// Retrieves the [$notifierClassName] from the current context.
  ///
  /// This is equivalent to `$providerClassName.of(context)` but
  /// provides a more concise syntax: `context.themeNotifier`.
  $notifierClassName get themeNotifier {
    return $providerClassName.of(this);
  }

  /// Retrieves the [$notifierClassName] from the current context (nullable).
  ///
  /// Returns null if no [$providerClassName] is found in the ancestry.
  $notifierClassName? get themeNotifierMaybe {
    return $providerClassName.maybeOf(this);
  }
}''';
  }

  // --- Token Value Helpers ---

  /// Generates a TextStyle constructor call from a token's value map.
  ///
  /// Parses fontSize, fontWeight, height, letterSpacing, fontFamily,
  /// and color properties from the token value.
  String? _generateTextStyle(dynamic value) {
    if (value is! Map<String, dynamic>) return null;

    final props = <String>[];

    if (value['fontSize'] is num) {
      props.add('fontSize: ${value['fontSize']}');
    }
    if (value['fontWeight'] is int) {
      props.add('fontWeight: FontWeight.w${value['fontWeight']}');
    }
    if (value['height'] is num) {
      props.add('height: ${value['height']}');
    }
    if (value['letterSpacing'] is num) {
      props.add('letterSpacing: ${value['letterSpacing']}');
    }
    if (value['fontFamily'] is String) {
      props.add("fontFamily: '${value['fontFamily']}'");
    }
    if (value['color'] is String) {
      final colorVal = TokenParser.parseColor(value['color']);
      if (colorVal != null) {
        props.add(
          'color: Color(0x${colorVal.toRadixString(16).padLeft(8, '0').toUpperCase()})',
        );
      }
    }

    if (props.isEmpty) return null;
    return 'TextStyle(${props.join(', ')})';
  }

  /// Extracts the field name from a token path (the last segment).
  String _fieldNameFromPath(String path) {
    return path.split('.').last;
  }

  // --- String Case Converters ---

  /// Converts a string to camelCase (e.g., "font-size" -> "fontSize").
  String _camelCase(String input) {
    if (input.isEmpty) return input;
    final parts = input.split(RegExp(r'[-_.]'));
    if (parts.isEmpty) return input;
    return parts.first.toLowerCase() +
        parts
            .skip(1)
            .map(
              (p) => p.isEmpty ? '' : '${p[0].toUpperCase()}${p.substring(1)}',
            )
            .join();
  }

  /// Converts a string to PascalCase (e.g., "font-size" -> "FontSize").
  String _pascalCase(String input) {
    if (input.isEmpty) return input;
    final parts = input.split(RegExp(r'[-_.]'));
    return parts
        .map((p) => p.isEmpty ? '' : '${p[0].toUpperCase()}${p.substring(1)}')
        .join();
  }

  /// Converts a dot-separated path to PascalCase.
  /// e.g., "display.h1" -> "DisplayH1"
  String _pascalCasePath(String path) {
    if (path.isEmpty) return path;
    return path.split('.').map((p) => _pascalCase(p)).join();
  }

  /// Strips the mode prefix from a token path.
  /// e.g., "light.color.brand.primary" -> "color.brand.primary"
  String _stripModePrefix(String path) {
    final parts = path.split('.');
    if (parts.isEmpty) return path;
    // Remove first part if it's a known mode
    if (parts[0] == 'light' || parts[0] == 'dark') {
      return parts.sublist(1).join('.');
    }
    return path;
  }

  /// Wraps text at a maximum width, returning lines with the given prefix.
  /// Used for formatting long descriptions in generated comments.
  List<String> _wrapText(String text, {int maxWidth = 80, String prefix = '// '}) {
    final words = text.split(' ');
    final lines = <String>[];
    var currentLine = prefix;

    for (final word in words) {
      // Check if adding this word would exceed max width
      if (currentLine.length + word.length + 1 > maxWidth) {
        lines.add(currentLine.trimRight());
        currentLine = '$prefix$word ';
      } else {
        currentLine += '$word ';
      }
    }

    // Add the last line if not empty
    if (currentLine.trimRight() != prefix.trimRight()) {
      lines.add(currentLine.trimRight());
    }

    return lines;
  }
