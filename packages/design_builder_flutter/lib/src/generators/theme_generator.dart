    );
    buffer.writeln();

    // Equality operators.
    buffer.writeln('  @override');
    buffer.writeln(
      '  bool operator ==(Object other) => other is $className && other._store == _store;',
    );
    buffer.writeln();
    buffer.writeln('  @override');
    buffer.writeln('  int get hashCode => _store.hashCode;');
    buffer.writeln('}');

    return buffer.toString();
  }

  /// Generates the theme helper classes that bridge AppTheme to resolvers.
